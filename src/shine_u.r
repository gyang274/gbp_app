# yg::`%+%` <- function(stringX, stringY) { return( paste0(stringX, stringY) ) }
`%+%` <- function(x, y) { 
  
  if ( is.environment(x) && is.environment(y) ) {
    
    return( ggplot2::`%+%`(x, y) )
    
  } else {
    
    return( paste0(x, y) )
    
  }
}
